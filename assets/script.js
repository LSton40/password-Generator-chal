// Assignment Code
//Declares the variable 'generateBtn' and links it to the HTML Button element with the id of 'generate'.
var generateBtn = document.querySelector("#generate");

//Declare the variables with values of the character types to be selected as strings
var lowercase = 'abcdefghijklmnopqrstuvwxyz';
var uppercase = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ';
var numbers = '0123456789';
var specialChar = "!#$%&'()*+,-./:;<=>?@[]^_`{|}~";

//The function that will generate the random password of the length and character types to be determined by the user.
function generatePassword() {

//Declares a variable ('passParameters') as an empty string to hold the character types selected by the user.
//Declares a variable ('generatedPassword') as an empty string to hold the password that is randomly generated by the function.
  var passParameters = '';
  var generatedPassword = '';

//Prompts the user to enter an inclusive number between 8 and 128, with alerts should they enter a number higher or lower than this range. Or lets user cancel out of prompt.
  var passwordLength = prompt('Choose a character length of at least 8 characters and no more than 128 characters.')

  if (passwordLength === null) {
    return '';
  }
  if (8 > passwordLength) {
    alert("You must choose a number larger than 8!")
    return generatePassword();
  }
  if (passwordLength > 128) {
    alert("You must choose a number smaller than 128!")
    return generatePassword();
  }


//Promps the user to select the types of characters they wish to include in the password and concatenates the selections to the 'passParameters' variable's string value.
//Alerts the user if they have refused all character types.*
  var lower = confirm('Do you want your password to include lowercase characters?');
  var upper = confirm('Do you want your password to include uppercase characters?');
  var numeric = confirm('Do you want your password to include numbers?');
  var special = confirm('Do you want your password to include special characters?');

  if (lower) {
    passParameters = passParameters.concat(lowercase);
  } 
  if (upper) {
    passParameters = passParameters.concat(uppercase);
  } 
  if (numeric) {
    passParameters = passParameters.concat(numbers);
  }
  if (special) {
    passParameters = passParameters.concat(specialChar);
  } 

  // console.log(passParameters);

  if (passParameters.length === 0) {
    var verify = confirm('You have to choose at least one type of character! Do you want to try again right now?');
    if (verify) {
    return generatePassword();
    } else {
      return '';
    }
  } 

  //Generates the random string of characters of the types and length selected by the user
  for (var i = 0; i < passwordLength; i++) {
    var randomization = Math.floor(Math.random() * passParameters.length);
    var random = passParameters[randomization];
    generatedPassword = generatedPassword.concat(random)
  }

  // console.log(randomization);
  // console.log(random);

  return generatedPassword

}

// Write password to the #password input
function writePassword() {
  var password = generatePassword();
  var passwordText = document.querySelector("#password");

  passwordText.value = password;

}

// Add event listener to generate button
generateBtn.addEventListener("click", writePassword);
